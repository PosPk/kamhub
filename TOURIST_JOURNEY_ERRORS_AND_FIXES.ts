export const ERROR_4 = `
ПРОБЛЕМА: Middleware не добавляет x-user-id header

СИМПТОМЫ:
- API endpoints возвращают 401 "пользователь не авторизован"
- Даже с валидным токеном

РЕШЕНИЕ:
1. Проверить middleware.ts
2. Убедиться что он добавляет x-user-id на основе JWT токена
3. Проверить порядок middleware

ИСПРАВЛЕННЫЙ middleware.ts:
export async function middleware(request: NextRequest) {
  const token = request.headers.get('authorization')?.replace('Bearer ', '');
  
  if (token) {
    // Декодировать JWT и добавить userId
    const decoded = decodeJWT(token);
    const requestHeaders = new Headers(request.headers);
    requestHeaders.set('x-user-id', decoded.sub);
    
    return NextResponse.next({
      request: {
        headers: requestHeaders,
      },
    });
  }
  
  return NextResponse.next();
}
`;

